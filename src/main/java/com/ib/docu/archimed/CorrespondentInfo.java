package com.ib.docu.archimed;

public class CorrespondentInfo {

	private String	id;
	private String	code;
	private String	name;

	public CorrespondentInfo() {
	}

	public String getCode() {
		return this.code;
	}

	public String getId() {
		return this.id;
	}

	public String getName() {
		return this.name;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}
}
